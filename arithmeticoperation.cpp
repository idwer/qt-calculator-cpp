#include "arithmeticoperation.h"

double ArithmeticOperation::Add(float left, float right)
{
    return left + right;
}

double ArithmeticOperation::Subtract(float left, float right)
{
    return left - right;
}

double ArithmeticOperation::Divide(float left, float right)
{
    return left / right;
}

double ArithmeticOperation::Multiply(float left, float right)
{
    return left * right;
}
